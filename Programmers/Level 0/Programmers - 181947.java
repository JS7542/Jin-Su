import java.util.*;

class Solution {
    /*
     * 두 정수 a와 b를 입력받아 합을 계산한다.
     * a + b = 결과 형식으로 덧셈식을 출력한다.
     */
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.print(a + " + " + b + " = " + (a + b));
    }
}
