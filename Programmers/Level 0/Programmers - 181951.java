import java.util.*;

class Solution {
    /*
     * 두 정수 a와 b를 입력받는다.
     * 각 값을 문제에서 지정한 이름과 줄바꿈 형식으로 출력한다.
     */
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        System.out.println("a = " + a);
        System.out.print("b = " + b);
    }
}
