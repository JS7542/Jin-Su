import java.util.*;

class Solution {
    /*
     * 공백으로 구분된 두 문자열을 입력받는다.
     * 별도의 공백 없이 두 문자열을 이어 붙여 출력한다.
     */
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String first = sc.next();
        String second = sc.next();
        System.out.print(first + second);
    }
}
